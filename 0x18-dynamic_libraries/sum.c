#include <stdlib.h>
#include <stdio.h>

/**
 * add - add two intergers
 * @a: first int
 * @b: second int
 * Return: Addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub two intergers
 * @a: first int
 * @b: second int
 * Return: Subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - mul two intergers
 * @a: first int
 * @b: second int
 * Return: Multiply
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - mod two intergers
 * @a: first int
 * @b: second int
 * Return: Mod
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * divi - divi two intergers
 * @a: first int
 * @b: second int
 * Return: Divide
 */
int divi(int a, int b)
{
	return (a / b);
}
